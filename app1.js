// Log to console

console.log('Hello World');
console.log(123); // numbers
console.log(true); // Boolean

let greeting = 'Hello'; // Variables
console.log(greeting);

console.log([1, 2, 3, 4]); // Arrays
console.log({ a: 1, b: 2, c: 3 }); // Objects Literal
console.table({ a: 1, b: 2, c: 3 }); // Prints objects as table

console.error('This is an error'); // Prints error
console.warn('This is a warning'); // Prints warning
console.clear(); // Clears console

console.time('Hello');

// If you want to know how long something taks to run. Put inbetween

console.timeEnd('Hello');

